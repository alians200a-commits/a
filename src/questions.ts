function toQuestion(item: RawQuestion): Question {
  const id = `p${item.page}-q${String(item.order).padStart(2, "0")}`;
  const base: Question = {
    id,
    sourcePage: item.page,
    sourceActivityOrder: 1,
    sourceItemOrder: item.order,
    sourceActivityLabel: "أسئلة المصدر — أسماء الاستفهام",
    sourceType: "وزاريات",
    year: item.year,
    type: item.kind,
    text: item.text,
    modelAnswer: item.answer,
  };

  if (item.kind === "poetry") {
    const [first, ...rest] = item.source.split(" / ");
    base.poetryLines = [{ first, second: rest.join(" / ") }];
  } else {
    base.verse = item.source;
  }

  return base;
}

